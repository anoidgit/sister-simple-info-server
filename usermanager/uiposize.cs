namespace usermanager
{
    class uiposize
    {
        public int width;
        public int height;
        public int top;
        public int left;

        public uiposize(int wid, int hei, int tp, int lft)
        {
            width = wid;
            height = hei;
            top = tp;
            left = lft;
        }
    }
}
